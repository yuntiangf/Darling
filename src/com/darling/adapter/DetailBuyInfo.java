public class DetailBuyInfo {
	
	private String detail_buy_name;
	private int detail_buy_image;
	
	public DetailBuyInfo(int detail_buy_image) {
		super();
		this.detail_buy_image = detail_buy_image;
	}

	public String getDetail_buy_name() {
		return detail_buy_name;
	}

	public void setDetail_buy_name(String detail_buy_name) {
		this.detail_buy_name = detail_buy_name;
	}

	public int getDetail_buy_image() {
		return detail_buy_image;
	}

	public void setDetail_buy_image(int detail_buy_image) {
		this.detail_buy_image = detail_buy_image;
	}
	
}
